// –§—É–Ω–∫—Ü–∏—è –¥–ª—è —Å–∫–ª–æ–Ω–µ–Ω–∏—è —Å–ª–æ–≤–∞ "–±–∞–ª–ª" –≤ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏ –æ—Ç —á–∏—Å–ª–∞
export const declinePoints = (count) => {
  const lastDigit = count % 10
  const lastTwoDigits = count % 100
  
  // –ò—Å–∫–ª—é—á–µ–Ω–∏—è –¥–ª—è 11-14
  if (lastTwoDigits >= 11 && lastTwoDigits <= 14) {
    return '–±–∞–ª–ª–æ–≤'
  }
  
  // 1 –±–∞–ª–ª
  if (lastDigit === 1) {
    return '–±–∞–ª–ª'
  }
  
  // 2, 3, 4 –±–∞–ª–ª–∞
  if (lastDigit >= 2 && lastDigit <= 4) {
    return '–±–∞–ª–ª–∞'
  }
  
  // 5, 6, 7, 8, 9, 0 –±–∞–ª–ª–æ–≤
  return '–±–∞–ª–ª–æ–≤'
}

// –ü–æ–ª—É—á–∏—Ç—å –ø–µ—Ä–µ–≤–æ–¥ –ø–æ –∫–ª—é—á—É
export const t = (key, lang = 'ru', replacements = {}) => {
  let text = translations[lang]?.[key] || translations['ru'][key] || key
  
  // –ó–∞–º–µ–Ω–∞ –ø–ª–µ–π—Å—Ö–æ–ª–¥–µ—Ä–æ–≤ {key} –Ω–∞ –∑–Ω–∞—á–µ–Ω–∏—è
  Object.keys(replacements).forEach(replKey => {
    text = text.replace(`{${replKey}}`, replacements[replKey])
  })
  
  return text
}

// –•—É–∫ –¥–ª—è –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏—è –ø–µ—Ä–µ–≤–æ–¥–æ–≤
export const useTranslation = (lang = 'ru') => {
  return {
    t: (key, replacements) => t(key, lang, replacements),
    lang
  }
}

/**
 * –ê–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏–π –ø–µ—Ä–µ–≤–æ–¥ –¥–∏–Ω–∞–º–∏—á–µ—Å–∫–æ–≥–æ –∫–æ–Ω—Ç–µ–Ω—Ç–∞ (–Ω–æ–≤–æ—Å—Ç–∏, –∞–∫—Ü–∏–∏, –æ–ø–∏—Å–∞–Ω–∏—è —É—Å–ª—É–≥)
 * –ò—Å–ø–æ–ª—å–∑—É–µ—Ç AI –ø–µ—Ä–µ–≤–æ–¥ —á–µ—Ä–µ–∑ backend API
 * 
 * @param {string} text - –¢–µ–∫—Å—Ç –¥–ª—è –ø–µ—Ä–µ–≤–æ–¥–∞
 * @param {string} lang - –¶–µ–ª–µ–≤–æ–π —è–∑—ã–∫
 * @param {string} sourceLang - –ò—Å—Ö–æ–¥–Ω—ã–π —è–∑—ã–∫ (–ø–æ —É–º–æ–ª—á–∞–Ω–∏—é 'ru')
 * @returns {Promise<string>} - –ü–µ—Ä–µ–≤–µ–¥–µ–Ω–Ω—ã–π —Ç–µ–∫—Å—Ç
 */
export const translateDynamicContent = async (
  text,
  lang = 'ru',
  sourceLang = 'ru'
) => {
  // –ï—Å–ª–∏ —è–∑—ã–∫ —Ä—É—Å—Å–∫–∏–π –∏–ª–∏ —Ç–µ–∫—Å—Ç –ø—É—Å—Ç–æ–π, –≤–æ–∑–≤—Ä–∞—â–∞–µ–º –æ—Ä–∏–≥–∏–Ω–∞–ª
  if (lang === 'ru' || lang === sourceLang || !text || !text.trim()) {
    return text
  }

  // –î–∏–Ω–∞–º–∏—á–µ—Å–∫–∏ –∏–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º —Ñ—É–Ω–∫—Ü–∏—é –ø–µ—Ä–µ–≤–æ–¥–∞
  try {
    const { translateText } = await import('./translate.js')
    return await translateText(text, lang, sourceLang)
  } catch (error) {
    console.error('Error importing translate utility:', error)
    return text
  }
}

/**
 * –•—É–∫ –¥–ª—è –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏—è –ø–µ—Ä–µ–≤–æ–¥–æ–≤ —Å –ø–æ–¥–¥–µ—Ä–∂–∫–æ–π –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–æ–≥–æ –ø–µ—Ä–µ–≤–æ–¥–∞ –¥–∏–Ω–∞–º–∏—á–µ—Å–∫–æ–≥–æ –∫–æ–Ω—Ç–µ–Ω—Ç–∞
 */
export const useTranslationWithAI = (lang = 'ru') => {
  return {
    t: (key, replacements) => t(key, lang, replacements),
    translateDynamic: (text, sourceLang = 'ru') => translateDynamicContent(text, lang, sourceLang),
    lang
  }
}

