def find_issue_by_title(title_keywords: list, team_key: Optional[str] = None) -> Optional[Dict]:
    """
    –ù–∞–π—Ç–∏ –∑–∞–¥–∞—á—É –ø–æ –∫–ª—é—á–µ–≤—ã–º —Å–ª–æ–≤–∞–º –≤ –Ω–∞–∑–≤–∞–Ω–∏–∏
    
    Args:
        title_keywords: –°–ø–∏—Å–æ–∫ –∫–ª—é—á–µ–≤—ã—Ö —Å–ª–æ–≤ –¥–ª—è –ø–æ–∏—Å–∫–∞
        team_key: –ö–ª—é—á –∫–æ–º–∞–Ω–¥—ã (–Ω–∞–ø—Ä–∏–º–µ—Ä, "MAR")
    
    Returns:
        Dict —Å –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–µ–π –æ –Ω–∞–π–¥–µ–Ω–Ω–æ–π –∑–∞–¥–∞—á–µ –∏–ª–∏ None
    """
    api_key = os.getenv("LINEAR_API_KEY")
    
    if not api_key:
        print("‚ùå LINEAR_API_KEY –Ω–µ –Ω–∞–π–¥–µ–Ω –≤ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö –æ–∫—Ä—É–∂–µ–Ω–∏—è")
        return None
    
    headers = {
        "Authorization": api_key,
        "Content-Type": "application/json"
    }
    
    # –ü–æ–ª—É—á–∞–µ–º team_id –µ—Å–ª–∏ —É–∫–∞–∑–∞–Ω team_key
    team_id = None
    if team_key:
        query = """
        query {
            teams {
                nodes {
                    id
                    key
                }
            }
        }
        """
        try:
            response = requests.post(
                LINEAR_API_URL,
                headers=headers,
                json={"query": query}
            )
            if response.status_code == 200:
                data = response.json()
                if "errors" not in data:
                    for team in data["data"]["teams"]["nodes"]:
                        if team["key"] == team_key:
                            team_id = team["id"]
                            break
        except Exception as e:
            print(f"‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ –ø–æ–ª—É—á–µ–Ω–∏–∏ –∫–æ–º–∞–Ω–¥—ã: {e}")
            return None
    
    # –ò—â–µ–º –∑–∞–¥–∞—á–∏
    query = """
    query($filter: IssueFilter) {
        issues(filter: $filter) {
            nodes {
                id
                identifier
                title
                description
                url
                team {
                    key
                    name
                }
            }
        }
    }
    """
    
    # –§–æ—Ä–º–∏—Ä—É–µ–º —Ñ–∏–ª—å—Ç—Ä
    filter_data = {}
    if team_id:
        filter_data["team"] = {"id": {"eq": team_id}}
    
    variables = {"filter": filter_data}
    
    try:
        response = requests.post(
            LINEAR_API_URL,
            headers=headers,
            json={"query": query, "variables": variables}
        )
        
        if response.status_code != 200:
            print(f"‚ùå –û—à–∏–±–∫–∞ HTTP: {response.status_code}")
            return None
        
        data = response.json()
        
        if "errors" in data:
            print(f"‚ùå –û—à–∏–±–∫–∞ GraphQL: {data['errors']}")
            return None
        
        # –ò—â–µ–º –∑–∞–¥–∞—á—É –ø–æ –∫–ª—é—á–µ–≤—ã–º —Å–ª–æ–≤–∞–º
        issues = data["data"]["issues"]["nodes"]
        for issue in issues:
            title_lower = issue["title"].lower()
            for keyword in title_keywords:
                if keyword.lower() in title_lower:
                    return {
                        "id": issue["id"],
                        "identifier": issue["identifier"],
                        "title": issue["title"],
                        "description": issue["description"] or "",
                        "url": issue["url"],
                        "team": issue["team"]["key"]
                    }
        
        return None
        
    except Exception as e:
        print(f"‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ –ø–æ–∏—Å–∫–µ –∑–∞–¥–∞—á–∏: {e}")
        return None


def update_issue_description(issue_id: str, new_description: str) -> Dict:
    """
    –û–±–Ω–æ–≤–∏—Ç—å –æ–ø–∏—Å–∞–Ω–∏–µ –∑–∞–¥–∞—á–∏ –≤ Linear
    
    Args:
        issue_id: ID –∑–∞–¥–∞—á–∏
        new_description: –ù–æ–≤–æ–µ –æ–ø–∏—Å–∞–Ω–∏–µ
    
    Returns:
        Dict —Å —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–º –æ–ø–µ—Ä–∞—Ü–∏–∏
    """
    api_key = os.getenv("LINEAR_API_KEY")
    
    if not api_key:
        return {
            "success": False,
            "error": "LINEAR_API_KEY –Ω–µ –Ω–∞–π–¥–µ–Ω –≤ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö –æ–∫—Ä—É–∂–µ–Ω–∏—è"
        }
    
    headers = {
        "Authorization": api_key,
        "Content-Type": "application/json"
    }
    
    mutation = """
    mutation($id: String!, $input: IssueUpdateInput!) {
        issueUpdate(id: $id, input: $input) {
            success
            issue {
                id
                identifier
                title
                description
                url
            }
        }
    }
    """
    
    variables = {
        "id": issue_id,
        "input": {
            "description": new_description
        }
    }
    
    try:
        response = requests.post(
            LINEAR_API_URL,
            headers=headers,
            json={"query": mutation, "variables": variables}
        )
        
        if response.status_code != 200:
            return {
                "success": False,
                "error": f"–û—à–∏–±–∫–∞ HTTP: {response.status_code} - {response.text}"
            }
        
        data = response.json()
        
        if "errors" in data:
            error_messages = [err.get("message", str(err)) for err in data["errors"]]
            return {
                "success": False,
                "error": "; ".join(error_messages)
            }
        
        if not data["data"]["issueUpdate"]["success"]:
            return {
                "success": False,
                "error": "–ù–µ —É–¥–∞–ª–æ—Å—å –æ–±–Ω–æ–≤–∏—Ç—å –∑–∞–¥–∞—á—É (success=false)"
            }
        
        issue = data["data"]["issueUpdate"]["issue"]
        
        return {
            "success": True,
            "issue_id": issue["id"],
            "identifier": issue["identifier"],
            "title": issue["title"],
            "url": issue["url"]
        }
        
    except Exception as e:
        return {
            "success": False,
            "error": f"–û—à–∏–±–∫–∞ –ø—Ä–∏ –æ–±–Ω–æ–≤–ª–µ–Ω–∏–∏ –∑–∞–¥–∞—á–∏: {e}"
        }


