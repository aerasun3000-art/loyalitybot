def get_all_issues_for_team(team_id: str) -> List[Dict]:
    """–ü–æ–ª—É—á–∏—Ç—å –≤—Å–µ –∑–∞–¥–∞—á–∏ –∫–æ–º–∞–Ω–¥—ã –∏–∑ Linear"""
