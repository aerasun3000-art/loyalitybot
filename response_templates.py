def search_templates(query: str) -> list:
    """
    –ò—â–µ—Ç —à–∞–±–ª–æ–Ω—ã –ø–æ –∫–ª—é—á–µ–≤–æ–º—É —Å–ª–æ–≤—É
    
    Args:
        query: –ü–æ–∏—Å–∫–æ–≤—ã–π –∑–∞–ø—Ä–æ—Å
    
    Returns:
        list: –°–ø–∏—Å–æ–∫ –Ω–∞–π–¥–µ–Ω–Ω—ã—Ö —à–∞–±–ª–æ–Ω–æ–≤
    """
    query_lower = query.lower()
    results = []
    
    for key, template in RESPONSE_TEMPLATES.items():
        if (query_lower in template['name'].lower() or 
            query_lower in template.get('use_case', '').lower() or
            query_lower in template['template'].lower()):
            results.append((key, template))
    
    return results






