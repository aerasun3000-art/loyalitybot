def get_template(template_key: str, variables: dict = None) -> dict:
    """
    –ü–æ–ª—É—á–∞–µ—Ç —à–∞–±–ª–æ–Ω –æ—Ç–≤–µ—Ç–∞
    
    Args:
        template_key: –ö–ª—é—á —à–∞–±–ª–æ–Ω–∞ (–Ω–∞–ø—Ä–∏–º–µ—Ä, 'greeting')
        variables: –°–ª–æ–≤–∞—Ä—å –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö –¥–ª—è –ø–æ–¥—Å—Ç–∞–Ω–æ–≤–∫–∏
    
    Returns:
        dict: –®–∞–±–ª–æ–Ω —Å –∑–∞–ø–æ–ª–Ω–µ–Ω–Ω—ã–º–∏ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–º–∏
    """
    if template_key not in RESPONSE_TEMPLATES:
        return None
    
    template_data = RESPONSE_TEMPLATES[template_key].copy()
    template_text = template_data['template']
    
    # –ó–∞–ø–æ–ª–Ω—è–µ–º –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ
    if variables and 'variables' in RESPONSE_TEMPLATES[template_key]:
        for var in RESPONSE_TEMPLATES[template_key].get('variables', []):
            value = variables.get(var, f'{{{var}}}')
            template_text = template_text.replace(f'{{{var}}}', str(value))
    
    template_data['message'] = template_text
    return template_data


